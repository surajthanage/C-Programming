void DisplayASCII()
{
	int i=0;
	
	for(i=0;i<=255;i++)
	{
		printf("%d\t %x\t %o\n",i,i,i);
	}
}
///////////////////////////////////////////////////////////////////////
// Display ASCII table
///////////////////////////////////////////////////////////////////////

int main()
{
	DisplayASCII();
	
	return 0;
}